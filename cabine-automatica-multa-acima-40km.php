        <h1 class="post-title text-center">Fique ligado! Cabine automática de pedágio multa motoristas a mais de 40km/h</h1>
        <img src="http://blogdofrete.com.br/img/radares-em-cabines-automaticas-de-pedagio.jpg" class="panorama" title="Fique ligado! Cabine automática de pedágio multa motoristas a mais de 40km/h" alt="novos fretes" />
        <h2>Novos <strong>radares</strong> nas cabines automáticas de <strong>pedágios</strong> de Coronel Macedo, Jaú e Boa Esperança do Sul, no sudoeste paulista, do tipo lombada eletrônica, já se encontram em operação desde a segunda-feira, 11.</h2>
 
        <p>Foram instalados, também, em oito praças de <strong>pedágios</strong> da região de Marília. Estes ainda aguardando autorização do Departamento de Estradas de Rodagem (DER) para iniciar a operação.</p>
        
        <p>Na pista de <strong>cobrança automática</strong>, os motoristas não param para realizar o pagamento, pois a cobrança é feita remotamente pela utilização de um dispositivo eletrônico instalado no veículo que libera a passagem.</p>
        
        <p>A <strong>fiscalização</strong> é uma exigência dos contratos mais recentes de concessão de rodovias, afirma a Agência de Transportes do Estado de São Paulo (Artesp). Tal medida surgiu com a finalidade de aumentar a segurança dos funcionários nas <strong>cabines de pagamento eletrônico</strong> e dos usuários, pois o <strong>excesso de velocidade</strong> tem causado acidentes quando, por exemplo, o pagamento não é autorizado e a sirene apita para o veículo aumentando o risco de colisão traseira, por exemplo.</p>
        
        <p>Segundo o Código de Trânsito Brasileiro, o motorista que passar com velocidade acima dos 40 km/h em até 20% será multado por infração média. Quando ultrapassado um limite entre 20% e 50%, a <strong>multa</strong> é grave, tornando-se gravíssima em velocidade acima de 50% do permitido.</p>

